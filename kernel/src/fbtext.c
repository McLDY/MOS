static uint32_t fg_color = 0xFFFFFF;
static uint32_t bg_color = 0x000000;
static uint32_t cursor_col = 0;
static uint32_t cursor_row = 0;

#define FONT_W 8
#define FONT_H 8

void fb_set_colors(uint32_t fg, uint32_t bg) {
    fg_color = fg;
    bg_color = bg;
}

void fb_clear_text(void) {
    clear_screen(bg_color);
    cursor_col = 0;
    cursor_row = 0;
}

void fb_set_cursor(uint32_t col, uint32_t row) {
    cursor_col = col;
    cursor_row = row;
}

static void put_char_at(char c, uint32_t col, uint32_t row) {
    if (!g_framebuffer) return;
    if (c < 32 || c > 127) c = '?';
    const uint8_t *glyph = font8x8_basic[(uint8_t)c - 32];

    uint32_t x0 = col * FONT_W;
    uint32_t y0 = row * FONT_H;

    for (uint32_t y = 0; y < FONT_H; y++) {
        uint8_t bits = glyph[y];
        for (uint32_t x = 0; x < FONT_W; x++) {
            if (bits & (1 << (7 - x))) {
                put_pixel(x0 + x, y0 + y, fg_color);
            } else {
                put_pixel(x0 + x, y0 + y, bg_color);
            }
        }
    }
}

void fb_putc(char c) {
    if (!g_framebuffer) return;

    if (c == '\n') {
        cursor_col = 0;
        cursor_row++;
    } else if (c == '\r') {
        cursor_col = 0;
    } else if (c == '\t') {
        cursor_col = (cursor_col + 4) & ~(4 - 1);
    } else if (c == '\b') {
        if (cursor_col > 0) cursor_col--;
        put_char_at(' ', cursor_col, cursor_row);
    } else {
        put_char_at(c, cursor_col, cursor_row);
        cursor_col++;
    }

    // 换行或列溢出处理
    if (cursor_col * FONT_W >= g_framebuffer->framebuffer_width) {
        cursor_col = 0;
        cursor_row++;
    }
    if ((cursor_row + 1) * FONT_H > g_framebuffer->framebuffer_height) {
        // 简单滚动：将屏幕上移一行字符（FONT_H 像素），再清空最后一行
        uint32_t src_y = FONT_H;
        uint32_t dst_y = 0;
        uint32_t copy_h = g_framebuffer->framebuffer_height - FONT_H;
        // 手动按行拷贝（逐字节）
        uint8_t *fb = (uint8_t*)(uintptr_t)g_framebuffer->framebuffer_addr;
        for (uint32_t y = 0; y < copy_h; y++) {
            uint8_t *dst = fb + (dst_y + y) * g_framebuffer->framebuffer_pitch;
            uint8_t *src = fb + (src_y + y) * g_framebuffer->framebuffer_pitch;
            // copy one scanline
            for (uint32_t i = 0; i < g_framebuffer->framebuffer_pitch; i++) {
                dst[i] = src[i];
            }
        }
        // 清空最后 FONT_H 行
        uint32_t clear_start = copy_h;
        for (uint32_t y = clear_start; y < g_framebuffer->framebuffer_height; y++) {
            uint8_t *row = fb + y * g_framebuffer->framebuffer_pitch;
            for (uint32_t x = 0; x < g_framebuffer->framebuffer_pitch; x++) {
                // 写 bg color per-byte according to bpp/format
                row[x] = 0;
            }
        }
        cursor_row = (g_framebuffer->framebuffer_height / FONT_H) - 1;
    }
}

void fb_puts(const char *s) {
    while (*s) {
        fb_putc(*s++);
    }
}